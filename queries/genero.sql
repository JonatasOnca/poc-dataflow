SELECT 
    CAST(GEN_ATIVO AS SIGNED) AS GEN_ATIVO,
    DATE_FORMAT(GEN_DT_ATUALIZACAO, "%Y-%m-%d %H:%i:%s.%f") AS GEN_DT_ATUALIZACAO,
    DATE_FORMAT(GEN_DT_CRIACAO, "%Y-%m-%d %H:%i:%s.%f") AS GEN_DT_CRIACAO,
    CAST(GEN_ID AS SIGNED) AS GEN_ID,
    CAST(GEN_NOME AS CHAR) AS GEN_NOME
FROM Saev.genero
